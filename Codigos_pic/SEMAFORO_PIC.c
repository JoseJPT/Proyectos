#include <16f877a.h>
#fuses xt,nowdt
#USE DELAY (CLOCK = 4M)


VOID MAIN () {
WHILE   (TRUE)
{
 OUTPUT_HIGH(PIN_A0);
OUTPUT_HIGH(PIN_A2);
OUTPUT_HIGH(PIN_A5);
DELAY_MS(500);

OUTPUT_LOW(PIN_A0);
OUTPUT_LOW(PIN_A2);
OUTPUT_LOW(PIN_A5);
DELAY_MS(100);

 OUTPUT_HIGH(PIN_A1);
OUTPUT_HIGH(PIN_A2);
OUTPUT_HIGH(PIN_B0);
DELAY_MS(500);

OUTPUT_LOW(PIN_A1);
OUTPUT_LOW(PIN_A2);
OUTPUT_LOW(PIN_B0);
DELAY_MS(100);
 
 OUTPUT_HIGH(PIN_A0);
OUTPUT_HIGH(PIN_A3);
OUTPUT_HIGH(PIN_B0);
DELAY_MS(500);

OUTPUT_LOW(PIN_A0);
OUTPUT_LOW(PIN_A3);
OUTPUT_LOW(PIN_B0);
DELAY_MS(100);
}
}

